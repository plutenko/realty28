-- Координаты переезжают с уровня ЖК на уровень корпуса (building):
-- в реальности корпуса одного ЖК (Лазурный берег, Дальневосточный талисман,
-- Жемчужина Амура и т.п.) часто стоят в разных местах, и одного пина на ЖК
-- мало.
--
-- 061_complex_geo.sql добавила lat/lng в complexes — поля оставляем
-- висящими (на случай, если 061 уже применили в каком-то окружении),
-- они просто не используются. Дроп будет отдельной миграцией позже.

ALTER TABLE buildings
  ADD COLUMN IF NOT EXISTS lat numeric,
  ADD COLUMN IF NOT EXISTS lng numeric;

COMMENT ON COLUMN buildings.lat IS 'Широта корпуса для карты (нет = не показываем пин)';
COMMENT ON COLUMN buildings.lng IS 'Долгота корпуса для карты (нет = не показываем пин)';
